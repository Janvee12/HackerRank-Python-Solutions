# Problem: Arithmetic Operators
# Platform: HackerRank
# Approach: Take two integers as input and perform basic arithmetic operations
# Time Complexity: O(1)
# Space Complexity: O(1)

if __name__ == '__main__':
    
    # Step 1: Take input values
    a = int(input())
    b = int(input())
    
    # Step 2: Perform operations
    # Addition
    print(a + b)
    
    # Subtraction
    print(a - b)
    
    # Multiplication
    print(a * b)